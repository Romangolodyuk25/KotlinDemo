fun main(args:Array<String>) {
    // :Int, :Long, :Short - Тип мы указываем тогда, когда не знаем какое значенеие ему присвоить
    var myInt: Int = 25 // Int - Integer (целые цислы) хранит в себе целые числа от -2,5 млрд до +2,5 млрд (32 бита)
    var myShort: Short = 25 // хранит в себе целые числа на 16 бит
    var myLong: Long = 25 // Хранит в себе более длинные целые числа 64 бита

   // myInt = 26
   // println("$myInt") // так как переменная VAR она изменяемая

    var myString: String = "Hello world"; // хранит в себе целые строки
    var myChar: Char = 'k' // Хранит в себе различный символ букву

    var myFloat: Float = 5.223525f // хранит в себе числа с запятой в конце ставится f
    var myDouble: Double = 3.14 //Хранит в себе так же числа с плавающей запятой но в большем объема

    var myBoolean: Boolean = true // Boolean - Хранит в себе значение true или false
}

